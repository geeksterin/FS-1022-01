import java.io.*;
import java.util.*;

public class Solution {

    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        
        int n = scn.nextInt();
        
        
        
        int i = 0;
        while(i <= n){
            System.out.println(i);
            i++;
        }
        
        
    }
}